#include <stdio.h>

int main() {
    // กำหนดค่าตัวแปรเริ่มต้น
    int a = 5, b = 17;
    float c = 8.5, d = 4.0;

    // 1. แสดงค่าเริ่มต้นของตัวแปรทั้งหมด
    printf("ค่าเริ่มต้น: a = %d, b = %d, c = %.1f, d = %.1f\n", a, b, c, d);

    // 2. ผลการคำนวณของ d + a
    printf("d + a = %.1f\n", d + a); // 4.0 + 5 = 9.0

    // 3. ผลการคำนวณของ a - b
    printf("a - b = %d\n", a - b); // 5 - 17 = -12

    // 4. ผลการคำนวณของ c * d
    printf("c * d = %.2f\n", c * d); // 8.5 * 4.0 = 34.00

    // 5. ผลการคำนวณของ a * c
    printf("a * c = %.2f\n", a * c); // 5 * 8.5 = 42.50

    // 6. ผลการคำนวณของ c / d
    printf("c / d = %.2f\n", c / d); // 8.5 / 4.0 = 2.13

    // 7. ผลการคำนวณของ b / c
    printf("b / c = %.2f\n", b / c); // 17 / 8.5 = 2.00

    // 8. ผลการคำนวณของ a % b
    printf("a %% b = %d\n", a % b); // 5 % 17 = 5

    // 9. ผลการคำนวณของ c % a
    // printf("c %% a = %.2f\n", c % a); 
    // ✘ Error: ไม่สามารถใช้ % กับ float ได้ในภาษา C
    // ✔ แก้ไขโดยแปลง c เป็น int ก่อน
    printf("((int)c) %% a = %d\n", ((int)c) % a); // (int)8.5 = 8 -> 8 % 5 = 3

    // 10. ผลการคำนวณของ c % d
    // printf("c %% d = %.2f\n", c % d);
    // ✘ Error: ไม่สามารถใช้ % กับ float ได้
    // ✔ หากต้องการหาเศษของการหาร float ใช้ fmod จาก math.h แทน
    #include <math.h>
    printf("fmod(c, d) = %.2f\n", fmod(c, d)); // 8.5 % 4.0 = 0.5

    return 0;
}
