#include <stdio.h>

int main() {
    // ----------------------------
    // ส่วนของ w06-02-01 (int i, j, k)
    // ----------------------------
    int i = 1, j = 2, k; // บรรทัดที่ 1: กำหนดค่าเริ่มต้นให้ i = 1, j = 2
    printf("w06-02-01:\n");
    printf("1. i = %d, j = %d, k = uninitialized\n", i, j);

    k = i + j; // บรรทัดที่ 2: k = 1 + 2 = 3
    printf("2. i = %d, j = %d, k = %d\n", i, j, k);

    i = i + (k * j); // บรรทัดที่ 3: i = 1 + (3 * 2) = 1 + 6 = 7
    printf("3. i = %d, j = %d, k = %d\n", i, j, k);

    j = i / 2; // บรรทัดที่ 4: j = 7 / 2 = 3 (int division)
    printf("4. i = %d, j = %d, k = %d\n", i, j, k);

    k = i % 2; // บรรทัดที่ 5: k = 7 % 2 = 1
    printf("5. i = %d, j = %d, k = %d\n", i, j, k);

    i = (j + k) * 3; // บรรทัดที่ 6: i = (3 + 1) * 3 = 12
    printf("6. i = %d, j = %d, k = %d\n\n", i, j, k);

    // ----------------------------
    // ส่วนของ w06-02-02 (double x, y, z)
    // ----------------------------
    double x = 1.0, y = 2.0, z; // บรรทัดที่ 1: กำหนดค่าเริ่มต้น
    printf("w06-02-02:\n");
    printf("1. x = %.1f, y = %.1f, z = uninitialized\n", x, y);

    x = y + 5.0; // บรรทัดที่ 2: x = 2.0 + 5.0 = 7.0
    printf("2. x = %.1f, y = %.1f\n", x, y);

    y = x / 2.0; // บรรทัดที่ 3: y = 7.0 / 2.0 = 3.5
    printf("3. x = %.1f, y = %.1f\n", x, y);

    y = (x * 3.0) + 4.0; // บรรทัดที่ 4: y = (7.0 * 3.0) + 4.0 = 21.0 + 4.0 = 25.0
    printf("4. x = %.1f, y = %.1f\n", x, y);

    x = -0.5 - y; // บรรทัดที่ 5: x = -0.5 - 25.0 = -25.5
    printf("5. x = %.1f, y = %.1f\n", x, y);

    z = x + y; // บรรทัดที่ 6: z = -25.5 + 25.0 = -0.5
    printf("6. x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0;
}
